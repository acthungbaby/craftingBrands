                            <h3>STARBUCKS: BRINDANDO SERVICIO AL CLIENTE</h3>
                            <p>A inicios de la primera década del 2000, la célebre compañía de café, Starbucks, era una de las pocas empresas que no había sido afectada por la recesión que golpeó a Estados Unidos luego del 11 de septiembre.</p>
                            <p>Esto se debió a la aplicación de una agresiva estrategia comercial que tenía como objetivo crear y mantener una cultura de café, y una experiencia en torno al consumo de café. La estrategia consistía en 3 componentes:</p>
                            <p>1. Servir el café de mayor calidad del mundo—procedente de África, América Central y Sudamérica, y del Pacífico Asiático—controlando la cadena de suministro para poder así asegurar el comercio justo;</p>
                            <p>2. Ofrecer un servicio óptimo: asegurar la “intimidad con el cliente”, reconociendo al cliente por su nombre, y recordando lo que suele pedir; </p>
                            <p>3. Crear un ambiente acogedor: “Llegar al cliente ahí donde trabaja, viaja, compra y cena”, y hacer que la gente quiera quedarse en el local.</p>
                            <p>Como desde sus inicios Starbucks ya tenía cubierto el primer punto, sus directores decidieron enfocarse en los dos últimos componentes de la estrategia comercial y en la innovación de sus productos.</p>
                            <p>Para expandir su venta minorista, Starbucks abrió locales en zonas urbanas con tasas salariales altas, en lugares de mucho tráfico y gran visibilidad. La empresa decoraba los locales como una sala, con sofás, mesas cómodas y wifi como para incentivar al cliente a quedarse por más tiempo.</p>
                        </div>
                        <div class="casosDer">
                            <p class="first">En cuanto al servicio, la compañía se enfocó en acrecentar la eficiencia del barista. Starbucks monitoreaba el desempeño del servicio utilizando varios parámetros:</p>
                            <p>1. La “foto instantánea del consumidor”: cada local era visitado por un comprador anónimo 3 veces por semestre, y evaluaba 4 criterios de “servicio básico”: servicio, limpieza, calidad del producto y velocidad del servicio.</p>
                            <p>2. Se evaluaba también el “servicio legendario”, que se basa en crear una experiencia memorable para el cliente para que quiera volver y contar su experiencia. Este “servicio legendario” se realiza a través de conversaciones con los clientes, reconociéndolos por nombre o su bebida favorita, etc.</p>
                            <p>Por último, la empresa decidió invertir tiempo y recursos en la innovación de productos, y comenzó a lanzar nuevos productos de forma regular (por ejemplo, lanzaba una bebida caliente nueva cada Navidad).</p>
                            <p>Fueron estas acciones las que ayudaron a Starbucks a llegar al coloso del café que es hoy, con aproximadamente 18.000 locales en más de 60 países del mundo.</p>
                            <p class="fuente">Fuente: (Moon, Youngme and Quelch, John. Starbucks: Delivering Customer Service. Case Study. Boston. Harvard Business Publishing, 2005. Print.</p>
