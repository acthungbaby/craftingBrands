                            <h3>CASO HARLEY DAVIDSON POSSE RIDE: CREAR COMUNIDAD DE MARCA</h3>
                            <p>En 1983, Harley-Davidson Motor Company creó un club de entusiastas de propietarios de motocicletas Harley-Davidson llamado “Harley Owners Group” (HOG), para neutralizar la mala imagen de los motoristas, fomentar la utilización de motocicletas, y crear una de las comunidades de marca más conocidas a nivel mundial. </p>
                            <p>El objetivo principal de la creación de este club fue el de “fomentar la experiencia del estilo de vida Harley-Davidson y acercar la empresa a sus clientes”. Los directivos de la empresa inclusive participan de los rallies organizados por el HOG—como el “Border-to-Border Posse Ride”, que cruza el territorio de EEUU con 400 motociclistas con Harleys—siguiendo la “filosofía de proximidad al cliente”.</p>
                            <p>Todo propietario de una Harley puede inscribirse al club, con un año de membresía gratuita por la compra de una nueva motocicleta. Las tiendas oficiales de Harley-Davidson abren capítulos del HOG a nivel local (inclusive hay un grupo en Paraguay: <a href="http://www.harleyclubparaguay.com.py" target="_blank">www.harleyclubparaguay.com.py</a>) para que sus socios puedan conocerse, organizar rallies, salir en sus motos, etc. Los miembros del HOG también gozan de ventajas tales como: una inscripción a la revista “HOG Tales”, seguro, servicio de asistencia en carretera, cursos de conducción segura, apoyo en rallies, camisetas y pins de los rallies. </p>
                        </div>
                        <div class="casosDer">
                            
                            <p class="first">Los rallies, o “posses”, como son conocidos en inglés, surgieron como idea para reunir a todos los motociclistas en una gran comunidad. Participan socios regionales, nacionales e internacionales para recorrer largas distancias en moto (empiezan y acaban en lugares distintos). Un “pasaporte” incluye el itinerario y los puntos de control por el camino. Estos puntos de control suelen ser tiendas oficiales que visan los pasaportes. Esto motiva a los participantes a parar en cada control para conseguir distintos sellos, y comprar merchandising de la marca. Los itinerarios suelen contar con rutas secundarias interesantes para salir del camino de la autopista, y fomentar el espíritu de aventura de los participantes. </p>
                            <p>Fue con la creación del Harley Owners Group, y sus actividades asociadas, que la marca logró unir a sus clientes a través de su pasión compartida por viajar en motocicleta. HOG logró que sus rallies se conviertan en una tradición, un reto de verdad, y una misión de supervivencia, fomentando la sensación de comunidad.</p>
                            <p class="fuente">Fuente: Fournier, Susan, et.al. Harley-Davidson Posse Ride: Building Brand Community. Case Study. Boston. Harvard Business Publishing, 2000. Print.</p>
